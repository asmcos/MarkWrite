/**
 * 根据 ~/.config/markwrite/sync-servers.json 写入 lib/eventstore-vendor/config.cjs，
 * 供 eventstore-vendor/esclient.cjs 使用（与 eventstore-profile.js 注释一致）。
 */
const fs = require('fs');
const path = require('path');

const CONFIG_OUT = path.join(__dirname, 'eventstore-vendor', 'config.cjs');

function pickActiveServer(data) {
  const servers = Array.isArray(data && data.servers) ? data.servers : [];
  const activeId = (data && data.activeId) || (servers[0] && servers[0].id) || null;
  return servers.find((s) => s && s.id === activeId) || servers[0] || null;
}

/**
 * @param {string} syncConfigFilePath - 一般为 sync-servers.json 绝对路径
 */
function writeEventstoreConfigFromSync(syncConfigFilePath) {
  let esserver = 'ws://127.0.0.1:8080/';
  let uploadpath = 'http://127.0.0.1:8081/uploads/';
  let sitename = '辰龙文档中心';
  let domain = 'http://localhost:5173';

  try {
    if (syncConfigFilePath && fs.existsSync(syncConfigFilePath)) {
      const raw = fs.readFileSync(syncConfigFilePath, 'utf8');
      const data = JSON.parse(raw);
      const active = pickActiveServer(data);
      if (active && typeof active === 'object') {
        if (typeof active.esserver === 'string' && active.esserver.trim()) esserver = active.esserver.trim();
        if (typeof active.uploadpath === 'string' && active.uploadpath.trim()) uploadpath = active.uploadpath.trim();
        if (typeof active.sitename === 'string' && active.sitename.trim()) sitename = active.sitename.trim();
        if (typeof active.domain === 'string' && active.domain.trim()) domain = active.domain.trim();
      }
    }
  } catch (_) {
    /* 使用默认 */
  }

  const q = (s) => JSON.stringify(s == null ? '' : String(s));
  const body = `/* Generated by MarkWrite from sync-servers.json — do not edit manually */
module.exports = {
  esserver: ${q(esserver)},
  uploadpath: ${q(uploadpath)},
  siteConfig: {
    name: ${q(sitename)},
    domain: ${q(domain)},
    beian: '',
    contact: '',
  },
};
`;

  fs.mkdirSync(path.dirname(CONFIG_OUT), { recursive: true });
  fs.writeFileSync(CONFIG_OUT, body, 'utf8');
}

module.exports = { writeEventstoreConfigFromSync };
